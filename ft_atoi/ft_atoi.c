#include <stdlib.h>

int	ft_atoi(char *s)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;

	while (*s == ' ' || (*s >= 9 && *s <= 13))
	s++;
	if (*s == '-')
		sign = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		res = res * 10 + *s - '0';
		s++;
	}
	return (sign * res);
}